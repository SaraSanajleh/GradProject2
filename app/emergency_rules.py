def check_emergency(symptoms_text: str) -> bool:
    """
    يحدد is_emergency فقط عند وجود كلمات مفتاحية حرجة (نزيف حاد، صعوبة تنفس، فقدان وعي...).
    أعراض عادية مثل صداع، تعب، غثيان، نزيف بسيط لا تجعل is_emergency = True.
    """
    text = symptoms_text.replace("ـ", "").strip().lower()
    simplified_keywords: Iterable[str] = (kw.replace("ـ", "").lower() for kw in EMERGENCY_KEYWORDS)
    return any(kw in text for kw in simplified_keywords)

